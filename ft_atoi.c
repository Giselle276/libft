#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		i;
	int		signo;
	int		res;

	i = 0;
	signo = 1;
	res = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
	{
		i++;
	}
	if (nptr[i] == '-')
	{
		signo = signo * -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = ((res * 10) + nptr[i]) - '0';
		i++;
	}
	res = signo * res;
	return (res);
}
